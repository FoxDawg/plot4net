namespace plot4net.Core.Options
{
    /// <summary>
    ///     Determines the mode of the axis grid.
    /// </summary>
    public enum GridMode
    {
        /// <summary>
        ///     Show no grid lines.
        /// </summary>
        None,

        /// <summary>
        ///     Show only horizontal grid lines.
        /// </summary>
        Horizontal,

        /// <summary>
        ///     Show vertical grid lines.
        /// </summary>
        Vertical,

        /// <summary>
        ///     Show horizontal and vertical grid lines.
        /// </summary>
        HorizontalAndVertical
    }
}