namespace plot4net.Core.Enum
{
    /// <summary>
    ///     The type of renderer used for image export.
    /// </summary>
    public enum RendererType
    {
        /// <summary>
        ///     No renderer.
        /// </summary>
        None,

        /// <summary>
        ///     Png type renderer.
        /// </summary>
        Png,

        /// <summary>
        ///     Bitmap type renderer.
        /// </summary>
        Bmp,

        /// <summary>
        ///     Jpeg type renderer.
        /// </summary>
        Jpeg,

        /// <summary>
        ///     Tiff type renderer.
        /// </summary>
        Tiff
    }
}