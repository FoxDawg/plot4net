namespace PlottingLib.Enum
{
    /// <summary>
    ///     Represents the line type of the plot.
    /// </summary>
    public enum LineType
    {
        /// <summary>
        ///     Plots no line at all.
        /// </summary>
        None,

        /// <summary>
        ///     Performs a scatter plot.
        /// </summary>
        Scatter,

        /// <summary>
        ///     Performs a line plot.
        /// </summary>
        Line,

        /// <summary>
        ///     Performs scatter and line plot.
        /// </summary>
        ScatterAndLine
    }
}