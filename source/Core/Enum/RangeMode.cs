namespace plot4net.Core.Enum
{
    /// <summary>
    ///     Determines the range mode for the axes of the plot.
    /// </summary>
    public enum RangeMode
    {
        /// <summary>
        ///     Automatic range calculation based on the data of the plots.
        /// </summary>
        Auto,

        /// <summary>
        ///     Manual range setting via figure options.
        /// </summary>
        Manual
    }
}